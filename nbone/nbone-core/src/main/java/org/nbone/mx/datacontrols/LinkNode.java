package org.nbone.mx.datacontrols;

public class LinkNode {
	
	private String id;
	
	private LinkNode next;
	

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	public LinkNode getNext() {
		return next;
	}

	public void setNext(LinkNode next) {
		this.next = next;
	}

	

}
